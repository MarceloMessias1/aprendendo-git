package primeiro.projeto;

public class Variaveis {

	public static void main(String[] args) {

		String nome = "Marcelo";
		int numeroInteiro = 39;
		double numeroQuebrado = 1.65;
		boolean verdadeiroFalso = false;
		

		System.out.println("Variável do tipo inteiro pode receber " + numeroInteiro);
		System.out.println("\nVariável do tipo quebrado pode receber " + numeroQuebrado);
		System.out.println("\nVariável do tipo quebrado pode receber " + verdadeiroFalso);
		System.out.println("\nMeu nome é " +nome +".");
		System.out.println("\nMinha idade é " + numeroInteiro);
		System.out.println("\nMinha altura é " + numeroQuebrado);

		numeroInteiro = 10;
		System.out.println("\nO número é " +numeroInteiro);
		
		numeroInteiro = 20;
		System.out.println("\nO número é " +numeroInteiro);
		
		numeroInteiro = 30;
		System.out.println("\nO número é " +numeroInteiro);
		
		
		
	}

}
